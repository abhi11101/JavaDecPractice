package org.com.abhi.Chapter1_10.Chapter3;

public class Operators {
    public static void main(String[] args) {

        int x = 42;
        int y = x++;
        System.out.println("Y: "+ y + "\nX: "+ x);

        int a = 42;
        int b = ++a;
        System.out.println("A: "+ a + "\nB: "+ b);
    }
}
